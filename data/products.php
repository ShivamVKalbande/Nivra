function getProductsByCategory($category = null) {
  global $products;
  if ($category === null || $category === 'all') return $products;
  return array_filter($products, function($p) use ($category) {
      return $p['category'] === $category;
  });
}

function getProductById($id) {
  global $products;
  foreach ($products as $p) { if ($p['id'] === $id) return $p; }
  return null;
}
